package method4;

public class Demo {

	void m() {
		System.out.println("Method with no param");
	}
	
	void m(int p) {
		System.out.println("Method with 1 param");
	}
	
	void m(int p,int q) {
		System.out.println("Method with 2 param");
	}
	
	void m(int p,int q,int r) {
		System.out.println("Method with 3 param");
	}
}
