package operators;

public class RelationalOperators {

	public static void main(String[] args) {
		// <,>,<=,>=,!=,==
		
		System.out.println(9<10);
		System.out.println(9>10);
		
		int a=100,b=200;
		
		System.out.println(a<b);
		
		System.out.println(a!=b);
		System.out.println(a==b);
	}

}
